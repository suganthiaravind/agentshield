"""Emit Tier 2 skill files into a target repo (Phase F.4).

After Tier 1 (semgrep) completes, the emitter writes two buckets of files:

1. **Copilot contract** — into `<target>/.agentshield/`:
   - bootstrap / checklist / output-schema templates (Copilot reads them
     to run Tier 2)
   - `tier1-results.json` with the Tier 1 findings + a fingerprint hash
     for stale-detection

2. **Developer-facing fix-skill files** — into `<output_dir>/` (default
   `<cwd>/output/`): the three `agentshield-<source>-fixes.md` skills.
   Kept out of the target so reports + remediation guidance for *every*
   scan land in one stable, predictable place — easier to share, archive,
   or attach to a ticket without digging into a hidden `.agentshield/`
   folder per target.

It also idempotently appends `.agentshield/` to `<target>/.gitignore` so
the Copilot-contract artifacts don't get accidentally committed.

The CLI calls `emit_skills(...)` once per scan and prints
`copilot_prompt(...)` so the user knows what to paste into Copilot Chat.

Stale-detection: `compute_tier1_fingerprint` produces a SHA-256 over
sorted `(file, line, rule_id)` tuples. Copilot copies this verbatim
into `tier2-findings.json`. The merger (F.5) compares the two; mismatch
means the user re-ran Tier 1 after Tier 2, so the Tier 2 results are
stale and should be re-generated.

This module has no Tier 1 / Tier 2 / CLI dependencies — it only takes
already-computed findings as input. That keeps the emitter testable
without spinning up semgrep.
"""

