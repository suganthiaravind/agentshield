def ensure_gitignored(target_root: Path) -> bool:
    """Append `.agentshield/` to `<target>/.gitignore` if not present.

    Returns True if the file was modified (created or appended), False if
    the entry was already present (idempotent).

    Recognises any of `.agentshield/`, `.agentshield`, `.agentshield/*`
    as already-present so we don't add duplicate lines on every scan.
    """
    gi = target_root / ".gitignore"
    accepted_forms = {".agentshield/", ".agentshield", ".agentshield/*"}

    if not gi.exists():
        gi.write_text(f"{GITIGNORE_MARKER}\n{GITIGNORE_ENTRY}\n")
        return True

    content = gi.read_text()
    existing_lines = {ln.strip() for ln in content.splitlines()}
    if accepted_forms & existing_lines:
        return False

    sep = "" if content.endswith("\n") else "\n"
    gi.write_text(content + sep + f"\n{GITIGNORE_MARKER}\n{GITIGNORE_ENTRY}\n")
    return True


def _git_meta(target_root: Path) -> dict:
    """Return git branch, short commit, and repo name for target_root.
    Falls back to empty strings if git is unavailable or the directory
    is not a git repository."""
    def _run(*args: str) -> str:
        try:
            return subprocess.check_output(
                ["git", "-C", str(target_root)] + list(args),
                stderr=subprocess.DEVNULL,
                text=True,
            ).strip()
        except Exception:
            return ""

    branch = _run("rev-parse", "--abbrev-ref", "HEAD")
    if branch == "HEAD":
        branch = _run("describe", "--tags", "--exact-match") or _run("rev-parse", "--short", "HEAD")
    commit = _run("rev-parse", "--short", "HEAD")
    return {
        "branch": branch,
        "commit": commit,
        "repo_name": target_root.name,
    }


def emit_skills(
    target_root: Path,
    findings: list[dict],
    scanned_files: list[str],
    *,
    now_utc: datetime | None = None,
    output_dir: Path | None = None,
    scan_started_at: datetime | None = None,
) -> EmitResult:
    """Render Tier 2 contract files + tier1-results.json into the target,
    and fix-skill `.md` files into the developer-facing output directory.

    Args:
        target_root: Repository root the user passed to `agentshield scan`.
        findings: Tier 1 findings as plain dicts (each must have at least
            `file`, `line`, `rule_id`; the emitter passes the rest through
            for Copilot to read).
        scanned_files: List of repo-relative POSIX paths Tier 1 scanned.
        now_utc: Override for testing — defaults to datetime.now(UTC).
        output_dir: Where the fix-skill `.md` files land. Defaults to
            `<cwd>/output/`. Created if missing.

    Returns:
        EmitResult describing what was written, including `output_dir`
        and `fix_skill_files` so the CLI can surface both locations.

    Side effects:
        - Creates `<target>/.agentshield/` and writes 4 files there
          (3 Tier 2 templates + tier1-results.json).
        - Creates `<output_dir>/` and writes 3 fix-skill `.md` files.
        - May modify `<target>/.gitignore` to ignore `.agentshield/`.
        Always overwrites generated artifacts (templates +
        tier1-results.json + fix-skills are not user-editable).
    """
    target_root = Path(target_root)
    contract_dir = target_root / ".agentshield"
    contract_dir.mkdir(exist_ok=True)

    out_dir = Path(output_dir) if output_dir is not None else default_output_dir()
    out_dir.mkdir(parents=True, exist_ok=True)

    emitted: list[Path] = []
    fix_skill_emitted: list[Path] = []

    def _copy_template(src_name: str, dst: Path) -> None:
        src = SKILLS_DIR / src_name
        if not src.exists():
            raise FileNotFoundError(
                f"Bundled skill template missing: {src} "
                "(reinstall agentshield package)"
            )
        dst.write_text(src.read_text())

    for src_name, dst_name in TIER2_TEMPLATE_FILES.items():
        dst = contract_dir / dst_name
        _copy_template(src_name, dst)
        emitted.append(dst)

    for src_name, dst_name in FIX_SKILL_FILES.items():
        dst = out_dir / dst_name
        _copy_template(src_name, dst)
        emitted.append(dst)
        fix_skill_emitted.append(dst)

    fingerprint = compute_tier1_fingerprint(findings)
    now = now_utc or datetime.now(timezone.utc)
    ts = now.isoformat()
    if ts.endswith("+00:00"):
        ts = ts[:-6] + "Z"

    git = _git_meta(target_root)
    scan_duration_s: int | None = None
    if scan_started_at is not None:
        scan_duration_s = max(0, round((now - scan_started_at).total_seconds()))

    tier1_payload = {
        "tier": 1,
        "scanned_at": ts,
        "agentshield_tier1_fingerprint": fingerprint,
        "scanned_files": list(scanned_files),
        "findings": list(findings),
        "git_branch": git["branch"],
        "git_commit": git["commit"],
        "git_repo_name": git["repo_name"],
        "scan_duration_seconds": scan_duration_s,
    }
    tier1_path = contract_dir / "tier1-results.json"
    tier1_path.write_text(json.dumps(tier1_payload, indent=2) + "\n")
    emitted.append(tier1_path)

    prompts_path = emit_copilot_prompts_md(target_root)
    emitted.append(prompts_path)

    gitignore_updated = ensure_gitignored(target_root)

    return EmitResult(
        target_root=target_root,
        emitted_files=emitted,
        tier1_path=tier1_path,
        fingerprint=fingerprint,
        gitignore_updated=gitignore_updated,
        output_dir=out_dir,
        fix_skill_files=fix_skill_emitted,
    )


